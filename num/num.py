x = 5
y = 9
z = 1

# Print the initial values
print("Initial values:")
print("x =", x)
print("y =", y)
print("z =", z)

# Put the values in a list
values = [x, y, z]

# Sort the list in ascending order
values.sort()

# Update x, y, and z based on the sorted list
x, y, z = values

# Print the updated values
print("\nUpdated values in ascending order:")
print("x =", x)
print("y =", y)
print("z =", z)